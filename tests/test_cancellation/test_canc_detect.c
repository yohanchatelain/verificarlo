#define bit(A, i) ((A >> i) & 1)
#define min(a, b) ((a < b) ? a : b)
#define max(a, b) ((a > b) ? a : b)
#define bit_a_b(X, a, b)                                                       \
  (((X << (sizeof(X) * 8 - max(a, b) - 1)) >>                                  \
    (sizeof(X) * 8 - max(a, b) - 1)) &                                         \
   ((X >> min(a, b)) << min(a, b)))

static void print_double(double d) {
  uint64_t a = *((uint64_t *)&d);

  for (int i = 63; i >= 0; i--)
    (i == 63 || i == 52) ? printf("%lld ", bit(a, i))
                         : printf("%lld", bit(a, i));

  printf("\n");
}

static void print_float(float f) {
  uint32_t a = *((uint32_t *)&f);

  for (int i = 31; i >= 0; i--)
    (i == 31 || i == 23) ? printf("%d ", bit(a, i)) : printf("%d", bit(a, i));

  printf("\n");
}

void sort_d(float *restrict a, int n) {
  for (int i = 0; i < n; i++)
    for (int j = i + 1; j < n; j++)
      if (a[j] < a[i]) {
        float tmp = a[i];

        a[i] = a[j];
        a[j] = tmp;
      }
}

